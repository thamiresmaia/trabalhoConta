    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int conta = Integer.parseInt(poupancanum.getText());
        if(conta == poupanca.p.getNumero()){
            if(taxaRe.getText()==null || taxaRe.getText().trim().equals("")){
                poupanca.p.reajustar();
                JOptionPane.showMessageDialog(null, "Ajuste foi realizado");
                menu m = new menu();
                m.setVisible(true);
                this.dispose();
            }
            
            else{
                poupanca.p.reajustar(Double.parseDouble(taxaRe.getText()));
                JOptionPane.showMessageDialog(null, "Ajuste foi realizado");
                menu m = new menu();
                m.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

