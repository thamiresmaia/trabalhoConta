    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        int contaPegar = Integer.parseInt(numeroSaque.getText());
        if (contaPegar == conta.comum.getNumero()){
            rc = conta.comum;
        }
        else if(contaPegar == especial.e.getNumero()){
            rc = especial.e;
        }
        else if (contaPegar == poupanca.p.getNumero()){
            rc = poupanca.p;
        }
        else {
            saque s = new saque();
            s.setVisible(true);
            this.dispose();
        }
        C.sacar(Double.parseDouble(sacar.getText()));
        C.setValorSaque(Double.parseDouble(sacar.getText()));
        confirmarSaque confs;
        confs = new confirmarSaque(rc);
        confs.setVisible(true);
        this.dispose();
    
    }//GEN-LAST:event_jToggleButton2ActionPerformed

